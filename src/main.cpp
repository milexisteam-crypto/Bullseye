static inline int is_hex(char c){
    return (c>='0'&&c<='9')||
           (c>='a'&&c<='f')||
           (c>='A'&&c<='F');
}

static inline int hex_val(char c){
    if(c>='0'&&c<='9') return c-'0';
    if(c>='a'&&c<='f') return 10+(c-'a');
    if(c>='A'&&c<='F') return 10+(c-'A');
    return 0;
}

